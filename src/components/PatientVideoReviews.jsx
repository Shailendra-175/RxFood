import React, { useState, useEffect } from "react";
import VideoCard from "./VideoCard";
import dummyStories from "../data/dummyStories"; // adjust the path if needed
import "../App.css";

export default function PatientVideoReviews() {
  const [videos, setVideos] = useState([]);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    // Use dummy stories for now
    setVideos(dummyStories);
    setLoading(false);
  }, []);

  if (loading) return <p>Loading patient stories...</p>;

  return (
    <div className="patient-video-container">
      <h2>Patient Stories</h2>
      <div className="video-grid">
        {videos.length === 0 ? (
          <p>No patient stories available.</p>
        ) : (
          videos.map((video) => <VideoCard key={video.id} video={video} />)
        )}
      </div>
    </div>
  );
}





































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































    
    
    
  









































